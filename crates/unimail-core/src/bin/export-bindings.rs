use std::{env, fs, path::PathBuf};

use ts_rs::{Config, TS};
use unimail_core::{
    AccountAuthState, ApplicationInfo, ConnectedAccountSummary, CredentialStoreKind,
    GmailOnboardingCommandError, GmailOnboardingErrorCode, GmailOnboardingState,
    GmailOnboardingStatus, Provider, StorageCommandError, StorageErrorCode, StorageStatus,
};

fn exported_declaration<T: TS>(config: &Config) -> String {
    T::decl(config).replacen("type ", "export type ", 1)
}

fn generated_source() -> String {
    let config = Config::default();
    let declarations = [
        exported_declaration::<ApplicationInfo>(&config),
        exported_declaration::<CredentialStoreKind>(&config),
        exported_declaration::<StorageErrorCode>(&config),
        exported_declaration::<StorageStatus>(&config),
        exported_declaration::<StorageCommandError>(&config),
        exported_declaration::<Provider>(&config),
        exported_declaration::<AccountAuthState>(&config),
        exported_declaration::<GmailOnboardingState>(&config),
        exported_declaration::<GmailOnboardingErrorCode>(&config),
        exported_declaration::<GmailOnboardingCommandError>(&config),
        exported_declaration::<ConnectedAccountSummary>(&config),
        exported_declaration::<GmailOnboardingStatus>(&config),
    ]
    .join("\n\n");
    format!(
        "// This file is generated by `npm run generate:bindings`. Do not edit it manually.\n\nimport {{ invoke }} from \"@tauri-apps/api/core\";\n\n{declarations}\n\nexport function applicationInfo(): Promise<unknown> {{\n  return invoke(\"application_info\");\n}}\n\nexport function storageStatus(): Promise<unknown> {{\n  return invoke(\"storage_status\");\n}}\n\nexport function gmailOnboardingStatus(): Promise<unknown> {{\n  return invoke(\"gmail_onboarding_status\");\n}}\n\nexport function startGmailOnboarding(accountId: string | null): Promise<unknown> {{\n  return invoke(\"start_gmail_onboarding\", {{ accountId }});\n}}\n\nexport function cancelGmailOnboarding(flowId: string): Promise<unknown> {{\n  return invoke(\"cancel_gmail_onboarding\", {{ flowId }});\n}}\n\nexport function connectedAccounts(): Promise<unknown> {{\n  return invoke(\"connected_accounts\");\n}}\n"
    )
}

fn main() {
    let root = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(|path| path.parent())
        .expect("unimail-core must live at crates/unimail-core")
        .to_path_buf();
    let output = root.join("src/lib/ipc/bindings.ts");
    let parent = output.parent().expect("bindings path must have a parent");
    fs::create_dir_all(parent).expect("failed to create IPC bindings directory");

    fs::write(&output, generated_source()).expect("failed to write TypeScript IPC bindings");
    println!("generated src/lib/ipc/bindings.ts");
}

#[cfg(test)]
mod tests {
    use super::generated_source;

    #[test]
    fn source_exports_all_ipc_types_and_unknown_commands() {
        let source = generated_source();

        for type_name in [
            "ApplicationInfo",
            "CredentialStoreKind",
            "StorageErrorCode",
            "StorageStatus",
            "StorageCommandError",
            "Provider",
            "AccountAuthState",
            "GmailOnboardingState",
            "GmailOnboardingErrorCode",
            "GmailOnboardingCommandError",
            "ConnectedAccountSummary",
            "GmailOnboardingStatus",
        ] {
            assert!(source.contains(&format!("export type {type_name}")));
        }
        assert!(source.contains("applicationInfo(): Promise<unknown>"));
        assert!(source.contains("storageStatus(): Promise<unknown>"));
        assert!(source.contains("invoke(\"storage_status\")"));
        assert!(source.contains("gmailOnboardingStatus(): Promise<unknown>"));
        assert!(source.contains("startGmailOnboarding(accountId: string | null)"));
        assert!(source.contains("cancelGmailOnboarding(flowId: string)"));
        assert!(source.contains("connectedAccounts(): Promise<unknown>"));
    }
}
