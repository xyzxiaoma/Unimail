use std::{env, fs, path::PathBuf};

use ts_rs::{Config, TS};
use unimail_core::{
    AccountAuthState, ApplicationInfo, ConnectedAccountSummary, CredentialStoreKind,
    OAuthOnboardingCommandError, OAuthOnboardingErrorCode, OAuthOnboardingState,
    OAuthOnboardingStatus, Provider, StorageCommandError, StorageErrorCode, StorageStatus,
};

fn exported_declaration<T: TS>(config: &Config) -> String {
    T::decl(config).replacen("type ", "export type ", 1)
}

fn generated_source() -> String {
    let config = Config::default();
    let declarations = [
        exported_declaration::<ApplicationInfo>(&config),
        exported_declaration::<CredentialStoreKind>(&config),
        exported_declaration::<StorageErrorCode>(&config),
        exported_declaration::<StorageStatus>(&config),
        exported_declaration::<StorageCommandError>(&config),
        exported_declaration::<Provider>(&config),
        exported_declaration::<AccountAuthState>(&config),
        exported_declaration::<OAuthOnboardingState>(&config),
        exported_declaration::<OAuthOnboardingErrorCode>(&config),
        exported_declaration::<OAuthOnboardingCommandError>(&config),
        exported_declaration::<ConnectedAccountSummary>(&config),
        exported_declaration::<OAuthOnboardingStatus>(&config),
    ]
    .join("\n\n");
    format!(
        "// This file is generated by `npm run generate:bindings`. Do not edit it manually.\n\nimport {{ invoke }} from \"@tauri-apps/api/core\";\n\n{declarations}\n\nexport function applicationInfo(): Promise<unknown> {{\n  return invoke(\"application_info\");\n}}\n\nexport function storageStatus(): Promise<unknown> {{\n  return invoke(\"storage_status\");\n}}\n\nexport function oauthOnboardingStatus(provider: Provider): Promise<unknown> {{\n  return invoke(\"oauth_onboarding_status\", {{ provider }});\n}}\n\nexport function startOauthOnboarding(provider: Provider, accountId: string | null): Promise<unknown> {{\n  return invoke(\"start_oauth_onboarding\", {{ provider, accountId }});\n}}\n\nexport function cancelOauthOnboarding(provider: Provider, flowId: string): Promise<unknown> {{\n  return invoke(\"cancel_oauth_onboarding\", {{ provider, flowId }});\n}}\n\nexport function connectAuthorizationCodeAccount(provider: Provider, accountId: string | null, accountAddress: string, authorizationCode: string): Promise<unknown> {{\n  return invoke(\"connect_authorization_code_account\", {{ provider, accountId, accountAddress, authorizationCode }});\n}}\n\nexport function connectedAccounts(): Promise<unknown> {{\n  return invoke(\"connected_accounts\");\n}}\n"
    )
}

fn main() {
    let root = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(|path| path.parent())
        .expect("unimail-core must live at crates/unimail-core")
        .to_path_buf();
    let output = root.join("src/lib/ipc/bindings.ts");
    let parent = output.parent().expect("bindings path must have a parent");
    fs::create_dir_all(parent).expect("failed to create IPC bindings directory");

    fs::write(&output, generated_source()).expect("failed to write TypeScript IPC bindings");
    println!("generated src/lib/ipc/bindings.ts");
}

#[cfg(test)]
mod tests {
    use super::generated_source;

    #[test]
    fn source_exports_all_ipc_types_and_unknown_commands() {
        let source = generated_source();

        for type_name in [
            "ApplicationInfo",
            "CredentialStoreKind",
            "StorageErrorCode",
            "StorageStatus",
            "StorageCommandError",
            "Provider",
            "AccountAuthState",
            "OAuthOnboardingState",
            "OAuthOnboardingErrorCode",
            "OAuthOnboardingCommandError",
            "ConnectedAccountSummary",
            "OAuthOnboardingStatus",
        ] {
            assert!(source.contains(&format!("export type {type_name}")));
        }
        assert!(source.contains("applicationInfo(): Promise<unknown>"));
        assert!(source.contains("storageStatus(): Promise<unknown>"));
        assert!(source.contains("invoke(\"storage_status\")"));
        assert!(source.contains("oauthOnboardingStatus(provider: Provider): Promise<unknown>"));
        assert!(
            source.contains("startOauthOnboarding(provider: Provider, accountId: string | null)")
        );
        assert!(source.contains("cancelOauthOnboarding(provider: Provider, flowId: string)"));
        assert!(source.contains("connectAuthorizationCodeAccount(provider: Provider"));
        assert!(source.contains("invoke(\"connect_authorization_code_account\","));
        assert!(source.contains("connectedAccounts(): Promise<unknown>"));
    }
}
