        assert!(source.contains("invoke(\"list_inbox_messages\", { request })"));
        assert!(source.contains("invoke(\"fetch_message_remote_image\", { messageId, url })"));
        assert!(source.contains("getMessageDetail(messageId: string): Promise<unknown>"));
