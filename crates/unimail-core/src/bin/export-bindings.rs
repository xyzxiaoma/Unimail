use std::{env, fs, path::PathBuf};

use ts_rs::{Config, TS};
use unimail_core::ApplicationInfo;

fn main() {
    let root = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(|path| path.parent())
        .expect("unimail-core must live at crates/unimail-core")
        .to_path_buf();
    let output = root.join("src/lib/ipc/bindings.ts");
    let parent = output.parent().expect("bindings path must have a parent");
    fs::create_dir_all(parent).expect("failed to create IPC bindings directory");

    let declaration =
        ApplicationInfo::decl(&Config::default()).replacen("type ", "export type ", 1);
    let source = format!(
        "// This file is generated by `npm run generate:bindings`. Do not edit it manually.\n\nimport {{ invoke }} from \"@tauri-apps/api/core\";\n\n{declaration}\n\nexport function applicationInfo(): Promise<unknown> {{\n  return invoke(\"application_info\");\n}}\n"
    );

    fs::write(&output, source).expect("failed to write TypeScript IPC bindings");
    println!("generated {}", output.display());
}
